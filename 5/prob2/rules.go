package main

// Pair represents a pair of values
type Pair struct {
	Key   int
	Value int
}

// Function to check if a specific key-value pair exists
func containsPair(pairs []Pair, key, value int) bool {
    for _, pair := range pairs {
        if pair.Key == key && pair.Value == value {
            return true
        }
    }
    return false
}

